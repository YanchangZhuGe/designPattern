package com.bgd.api.common.security.arithmetic.util;

public class ObjectChangeItem {
	private String fieldNameText;
	private String fieldName;
	private String orgVal;
	private String newVal;


	/**
	 * @return String
	 */
	public String getFieldNameText() {
		return fieldNameText;
	}

	/**
	 * @param fieldNameText String
	 */
	public void setFieldNameText(String fieldNameText) {
		this.fieldNameText = fieldNameText;
	}

	/**
	 * @return String
	 */
	public String getFieldName() {
		return fieldName;
	}

	/**
	 * @param fieldName String
	 */
	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}

	/**
	 * @return String
	 */
	public String getOrgVal() {
		return orgVal;
	}

	/**
	 * @param orgVal String
	 */
	public void setOrgVal(String orgVal) {
		this.orgVal = orgVal;
	}

	/**
	 * @return String
	 */
	public String getNewVal() {
		return newVal;
	}

	/**
	 * @param newVal String
	 */
	public void setNewVal(String newVal) {
		this.newVal = newVal;
	}


}
