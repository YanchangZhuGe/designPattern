CREATE OR REPLACE VIEW DEBT_V_BDJH_ND AS
SELECT '2015' GUID,'2015' CODE,'2015' NAME FROM DUAL
UNION ALL
SELECT '2016' GUID,'2016' CODE,'2016' NAME FROM DUAL
UNION ALL
SELECT '2017' GUID,'2017' CODE,'2017' NAME FROM DUAL
UNION ALL
SELECT '2018' GUID,'2018' CODE,'2018' NAME FROM DUAL
UNION ALL
SELECT '2019' GUID,'2019' CODE,'2019' NAME FROM DUAL
UNION ALL
SELECT TO_CHAR(SYSDATE,'YYYY') GUID,TO_CHAR(SYSDATE,'YYYY') CODE,TO_CHAR(SYSDATE,'YYYY') NAME FROM DUAL
UNION ALL
SELECT 
      TO_CHAR(ADD_MONTHS(SYSDATE,12),'YYYY') GUID,
      TO_CHAR(ADD_MONTHS(SYSDATE,12),'YYYY') CODE,
      TO_CHAR(ADD_MONTHS(SYSDATE,12),'YYYY') NAME 
 FROM DUAL ORDER BY CODE ASC